// While in java
public class while_loop{ 
	public static void main(String args[]){
        int a = 3,i=0;
        while(i<a){ // from 0 to the value of a, it traverse till max size. which here is 3 it goes inside the block only if it's true
            System.out.println("the value of i is " +i);
            i++; // an increment after printing the value
            // transfer goes to while statement till condition becomes false.
        }
    }
}
